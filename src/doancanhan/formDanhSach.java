    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
                    String ma,ht, ngay;
                    boolean gt;
                    double sg,dg;
                    ma=txtMa.getText();
                    ht=TxtTen.getText();
                    ngay=txtNgay.getText();
                    gt=Boolean.parseBoolean(txtGioiTinh.getText());
                    dg=Double.parseDouble(txtLuong.getText());
                    sg=Double.parseDouble(txtGio.getText());
                    Nguoi x;
                    if(btkhoa.isSelected()){
                        String khoa= txtChon.getText();
                        x = new GiangVien(khoa, ma, ht, ngay, gt, sg, dg);
                    }else{
                        String congviec=txtChon.getText();
                        x= new NhanVien(congviec, ma, ht, ngay, gt, sg, dg);
                    } if(a.them(x)==true) {
            loadDuLieu(a.getA());
        }else JOptionPane.showMessageDialog(null, "Đã có "+ma+" trong danh sách");
    }//GEN-LAST:event_btThemActionPerformed

