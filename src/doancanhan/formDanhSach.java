    public void loadDuLieu(ArrayList<Nguoi> a1){
        ArrayList<Nguoi> b= a.getA();
        while(dtm.getRowCount()>0) dtm.removeRow(0);
        for(Nguoi y:b){
            if(y instanceof GiangVien)
                dtm.addRow(new Object[]{y.getMa(),y.getHoTen(),y.getNgaysinh(),y.isGioiTinh(),y.getSoGio(),y.getDonGia(),((GiangVien) y).getKhoa(),y.tinhThanhTien()});
            else 
                dtm.addRow(new Object[]{y.getMa(),y.getHoTen(),y.getNgaysinh(),y.isGioiTinh(),y.getSoGio(),y.getDonGia(),((NhanVien) y).getCongViec(),y.tinhThanhTien()});;
        }
    }
